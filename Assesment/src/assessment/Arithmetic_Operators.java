package assessment;

public class Arithmetic_Operators {
	public static void main (String args[]) {
		int a=10;
		int b=3;
		System.out.println("a+b="+(a+b));
		System.out.println("a-b="+(a-b));
		System.out.println("a*b="+(a*b));
		System.out.println("a/b="+(a/b));
		System.out.println("a%b="+(a%b));
	}
}
