package assessment;

public class Increment_Decrement_Operators {
	public static void main(String args[]) {
		int x=5;
		int y=7;
		int m=3;
		int n=9;
		System.out.println("Increment operators -Pre Increment: "+(++x));
		System.out.println("Increment operators -Post Increment: "+(m++));
		System.out.println("Decrement operators -Pre Decrement: "+(--y));
		System.out.println("Decrement operators -Post Decrement: "+(n--));
	}
}
