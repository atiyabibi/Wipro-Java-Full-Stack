package assessment;

public class Bitwise_Operators {
	public static void main(String args[]) {
		int a=5;
		int b=3;
		System.out.println("Bitwise Ans :(a&b)= "+(a&b));
		System.out.println("Bitwise OR :(a|b)= "+(a|b));
		System.out.println("Bitwise Xor :(a^b)= "+(a^b));
		System.out.println("Bitwise Right shift :(a>>b)= "+(a>>b));
		System.out.println("Bitwise Left Shift :(a<<b)= "+(a<<b));
		System.out.println("Bitwise Unsigned Right shift :(a>>>b)= "+(a>>>b));
		System.out.println("Bitwise not :(~a)= "+(~a));		
	}
}
